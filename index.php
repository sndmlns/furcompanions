<script>
    $(document).ready(function() {
        $('#bookingForm').submit(function(event) {
            event.preventDefault(); // Prevent the form from submitting normally

            // Get form data
            var formData = {
                'name': $('#name').val(),
                'email': $('#email').val(),
                'reservation_date': $('#reservation_date').val(),
                'reservation_time': $('#reservation_time').val(),
                'service': $('#service').val()
            };

            // Send the form data to submit_booking.php using AJAX
            $.ajax({
                type: 'POST',
                url: 'submit_booking.php', // Replace with your PHP script URL
                data: formData,
                dataType: 'json',
                encode: true,
                success: function(data) {
                    // Handle success message
                    $('#successMessage').html(data.message).fadeIn();

                    // Make the success message disappear after 1.5 seconds
                    setTimeout(function() {
                        $('#successMessage').fadeOut();
                    }, 1500);
                },
                error: function(xhr, status, error) {
                    // Handle errors
                    console.error(xhr.responseText);
                    $('#successMessage').html('Error occurred. Please try again.').fadeIn();

                    // Make the error message disappear after 1.5 seconds
                    setTimeout(function() {
                        $('#successMessage').fadeOut();
                    }, 1500);
                }
            });
        });
    });
</script>






